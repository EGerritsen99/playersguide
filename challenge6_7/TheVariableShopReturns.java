package challenge6_7;

public class TheVariableShopReturns {

    public static void main (String args[]) {

        String string = "Goodbye!";
        int integerNumber = 87;
        double doubleNumber = 8.56586367894856325;
        boolean falseBoolean = true;
        float floatNumber = 3.5676878F;
        short shortNumber = 6453;
        long longNumber = 541527;
        byte byteNumber = 127;
        char character = 't';

        System.out.println("This is a string: " + string);
        System.out.println("This is an int: " + integerNumber);
        System.out.println("This is a double: " + doubleNumber);
        System.out.println("This is a boolean: " + falseBoolean);
        System.out.println("This is a float: " + floatNumber);
        System.out.println("This is a short: " + shortNumber);
        System.out.println("This is a long: " + longNumber);
        System.out.println("This is a byte: " + byteNumber);
        System.out.println("This is a char: " + character);

    }

}
